/** Inteiro pt-BR (ex.: 1312 → "1.312"). */
export const num = (n: number) => n.toLocaleString("pt-BR");

/** Quantas atividades já foram iniciadas (atual > 0). */
export function atividadesIniciadas(c: ProMakerConsultor): number {
  return ATIVIDADES.filter((a) => (c[a.key] as number) > 0).length;
}

/** Quantas atividades já bateram a meta (concluídas). */
export function atividadesConcluidas(c: ProMakerConsultor): number {
  return ATIVIDADES.filter((a) => (c[a.key] as number) >= a.meta).length;
}

