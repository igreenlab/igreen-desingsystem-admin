/** KPIs do topo (totais sobre o dataset mockado). */
export function proMakerKpis() {
  const pros = proMaker.filter((c) => c.isPro);
  const construcao = proMaker.filter((c) => !c.isPro);
  const marco = (n: number) => pros.filter((c) => c.diretosPro >= n).length;
  return {
    pros: pros.length,
    construcao: construcao.length,
    redeLicenciados: proMaker.length,
    p3: marco(3),
    p6: marco(6),
    p9: marco(9),
    p12: marco(12),
  };
}
